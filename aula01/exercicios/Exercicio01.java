package exercicios;

public class Exercicio01 {
    public static void main(String[] args) {
        // Soma
        System.out.println("17 +  5 = " + (17 + 5));
        // Subtracao
        System.out.println("17 -  5 = " + (17 - 5));
        // Multiplicacao
        System.out.println("17 *  5 = " + (17 * 5));
        // Divisao
        System.out.println("17 /  5 = " + (17 / 5));
        //Float
        System.out.println("17.0 /  5 = " + (17.0 / 5));
    }
}
